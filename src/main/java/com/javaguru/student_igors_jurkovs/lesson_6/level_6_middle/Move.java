import com.javaguru.teacher.codereview.CodeReview;

@CodeReview(approved = true)
class Move {

    private int x;
    private int y;

    Move(int x, int y) {
        this.x = x;
        this.y = y;
    }

    int getX() {
        return x;
    }

    int getY() {
        return y;
    }

}
