package com.javaguru.teacher.lesson_6.lessoncode;

class ExampleFor {

    public static final int MIN = 3;
    public static final int MAX = 5;

    public static void main(String[] args) {
        for (int i = MIN; i < MAX; i++) {
            System.out.println("i = " + i);
        }

        System.out.println();

        for (int i = MIN; i < MAX; i++) {
            System.out.println("i = " + i);
        }

        System.out.println();

        for (int i = MIN; i < MAX; i++) {
            System.out.println("i = " + i);
        }
    }
}
